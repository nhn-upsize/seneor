-- ============================================================================
-- 05. lv2_genre × genre 분포 확인 (초기 구조 파악용)
-- ============================================================================
-- 용도: 방치형·머지·PvP·PvE 가 어떤 ST 대표 장르(genre)에 속해있는지 확인
--       신규 lv2_genre 값이 추가되거나 매핑 변경이 있을 때 최초 점검
-- ============================================================================

SELECT
  genre,
  sub_genre,
  lv2_genre,
  COUNT(DISTINCT unified_app_id) AS app_count,
  COUNT(*) AS row_count
FROM dw_app_monthly
WHERE (sub_genre IS NOT NULL OR lv2_genre IS NOT NULL)
GROUP BY genre, sub_genre, lv2_genre
ORDER BY genre, sub_genre, lv2_genre;


-- ----------------------------------------------------------------------------
-- 참고: lv2_genre 값 체계
-- ----------------------------------------------------------------------------
-- • '방치형'  — 앱 종료·백그라운드 상태에서도 자동 진행되는 게임 (Role Playing 다수)
-- • '머지'    — 동일 오브젝트 2개 합성이 코어 메카닉 (Puzzle 다수)
-- • 'PvP'     — 유저 간 대결 (Card·Casino 중 포커/맞고 등)
-- • 'PvE'     — 기계 상대 (Card·Casino 중 슬롯/솔리테어/Monopoly GO 등)
-- ----------------------------------------------------------------------------
-- 참고: sub_genre 값 체계
-- ----------------------------------------------------------------------------
-- • 'MMORPG'  — Role Playing 중 대규모 멀티플레이어 온라인 구조
-- • '비MMORPG' — Role Playing 중 MMORPG 외 전부
-- ----------------------------------------------------------------------------
-- Role Playing 앱에만 sub_genre 가 부여되며, 다른 장르는 NULL
-- lv2_genre 는 여러 장르에 걸쳐 부여 가능 (방치형은 RPG·Sim·Casual 등 다수)
